<div class="page">
	<h1>Privacy policy</h1>
	<p>
		VERT processes your image files entirely on your own device. When you add a file and
		convert it, the conversion runs in your browser using native codecs. Your files are
		never uploaded to a server, and no download is produced until you convert a file you
		added yourself.
	</p>

	<h2>Local processing</h2>
	<p>
		Image, audio, and document conversions happen locally. Only video conversion would use
		a remote server, and this build does not send your files anywhere.
	</p>

	<h2>What VERT stores</h2>
	<p>
		VERT keeps a small amount of state in your browser's local storage: your theme choice,
		the active conversion category, and your quality and metadata preferences. Your files
		and their converted results are held in memory only and are cleared when you reload.
	</p>

	<h2>No tracking</h2>
	<p>There are no ads and no analytics. Your conversions are your business.</p>
</div>
