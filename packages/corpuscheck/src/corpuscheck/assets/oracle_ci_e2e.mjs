// e2e stage of `corpuscheck oracle-ci`: run the task's canonical Playwright
// suite (solution/app/e2e.spec.mjs) against the already-serving app on :3000.
// Importable (no top-level side effects) so the pytest suite can exercise the
// stage directly against tiny fixture apps, mirroring oracle_ci_semantics.mjs.

import { spawn } from 'node:child_process';
import fs from 'node:fs';
import { createRequire } from 'node:module';
import path from 'node:path';

const CANONICAL_CONFIG = 'e2e.playwright.config.mjs';
const SPEC_NAME = 'e2e.spec.mjs';
const TASK_SUITE_DIR = 'e2e';
const TASK_SPEC_RE = /\.spec\.(mjs|cjs|js|ts|tsx|jsx)$/;
const CANONICAL_TEST_TITLES = new Set([
  'serves non-empty app with zero console errors',
  'webmcp surface is registered and well-formed',
  'reduced motion behaviorally suppresses animation',
  'no horizontal overflow at 375px',
]);

/** Spec files inside solution/app/e2e/ (task-owned per-criterion suites). */
export function taskSuiteSpecs(appDir) {
  const dir = path.join(appDir, TASK_SUITE_DIR);
  if (!fs.existsSync(dir) || !fs.statSync(dir).isDirectory()) return [];
  return fs
    .readdirSync(dir, { recursive: true })
    .map(String)
    .filter((f) => TASK_SPEC_RE.test(f))
    .sort();
}

/**
 * Decide how (or whether) the e2e suite runs for one app dir.
 * Returns one of:
 *   { mode: 'skip' }                       — neither e2e.spec.mjs nor e2e/*.spec.*;
 *                                            stage skips.
 *   { mode: 'missing-dependency', message }— specs exist, @playwright/test not
 *                                            resolvable from the app; stage fails.
 *   { mode: 'run', runs: [...] }           — one entry per suite layout found:
 *     { label: 'canonical', config: 'canonical-config', command } — app ships
 *       e2e.playwright.config.mjs ('-c' already appended), or
 *     { label: 'canonical', config: 'generated-config', command } — pin
 *       discovery to e2e.spec.mjs with a generated config, and/or
 *     { label: 'task-dir', config: 'generated-dir-config', command } — the
 *       task-owned e2e/ directory suite, pinned with a generated config.
 * Discovery is ALWAYS pinned through a `-c` config so an app-local
 * playwright.config.{js,ts,mjs} can never hijack which tests run.
 */
export function resolveE2eInvocation(appDir) {
  const hasCanonical = fs.existsSync(path.join(appDir, SPEC_NAME));
  const taskSpecs = taskSuiteSpecs(appDir);
  if (!hasCanonical && taskSpecs.length === 0) return { mode: 'skip' };
  const requireFromApp = createRequire(path.join(appDir, 'package.json'));
  try {
    requireFromApp.resolve('@playwright/test');
  } catch {
    const layouts = [];
    if (hasCanonical) layouts.push(SPEC_NAME);
    if (taskSpecs.length > 0) layouts.push(`${TASK_SUITE_DIR}/*.spec.*`);
    return {
      mode: 'missing-dependency',
      message:
        `Playwright suite files were found (${layouts.join(', ')}), but ` +
        '@playwright/test is not resolvable from the app ' +
        'after npm ci — add @playwright/test to solution/app devDependencies ' +
        '(use the app\'s existing copy; never install a second one)',
    };
  }
  // Prefer the app-local @playwright/test CLI over `npx` so nothing is ever
  // fetched from the network and PATH cannot substitute another playwright.
  let command;
  try {
    const packagePath = requireFromApp.resolve('@playwright/test/package.json');
    command = [process.execPath, path.join(path.dirname(packagePath), 'cli.js'), 'test'];
  } catch {
    command = ['npx', '--no-install', 'playwright', 'test'];
  }
  const runs = [];
  if (hasCanonical) {
    runs.push(
      fs.existsSync(path.join(appDir, CANONICAL_CONFIG))
        ? { label: 'canonical', config: 'canonical-config', command: [...command, '-c', CANONICAL_CONFIG] }
        : { label: 'canonical', config: 'generated-config', command: [...command] },
    );
  }
  if (taskSpecs.length > 0) {
    runs.push({ label: 'task-dir', config: 'generated-dir-config', command: [...command] });
  }
  return { mode: 'run', runs };
}

/** Config equivalent to the canonical one, pinned to exactly e2e.spec.mjs. */
export function generatedConfigSource(appDir) {
  return [
    '// Generated by corpuscheck oracle-ci: pins discovery to the canonical',
    '// e2e suite so app-local playwright configs cannot hijack it.',
    'export default {',
    `  testDir: ${JSON.stringify(appDir)},`,
    `  testMatch: ${JSON.stringify(SPEC_NAME)},`,
    '  timeout: 30_000,',
    '  retries: 0,',
    `  use: { baseURL: ${JSON.stringify('http://127.0.0.1:3000')} },`,
    '};',
    '',
  ].join('\n');
}

/** Pinned config for the task-owned e2e/ directory suite (any *.spec.* inside). */
export function generatedDirConfigSource(appDir) {
  return [
    '// Generated by corpuscheck oracle-ci: pins discovery to the task-owned',
    '// e2e/ directory so app-local playwright configs cannot hijack it.',
    'export default {',
    `  testDir: ${JSON.stringify(path.join(appDir, TASK_SUITE_DIR))},`,
    `  testMatch: ${JSON.stringify('**/*.spec.{mjs,cjs,js,ts,tsx,jsx}')},`,
    '  fullyParallel: true,',
    '  workers: 4,',
    '  timeout: 30_000,',
    '  retries: 0,',
    '  projects: [',
    '    {',
    "      name: 'functional',",
    "      testIgnore: '**/performance.spec.{mjs,cjs,js,ts,tsx,jsx}',",
    '    },',
    '    {',
    "      name: 'performance',",
    "      testMatch: '**/performance.spec.{mjs,cjs,js,ts,tsx,jsx}',",
    "      dependencies: ['functional'],",
    '      fullyParallel: false,',
    '    },',
    '  ],',
    `  use: { baseURL: ${JSON.stringify('http://127.0.0.1:3000')} },`,
    '};',
    '',
  ].join('\n');
}

/** Criterion names authored in one task's dimension TOMLs. */
export function rubricCriterionNames(taskDir) {
  const testsDir = taskDir ? path.join(taskDir, 'tests') : null;
  if (!testsDir || !fs.existsSync(testsDir)) return [];
  const names = new Set();
  const files = fs
    .readdirSync(testsDir, { recursive: true })
    .map(String)
    .filter((file) => file.endsWith('.toml'));
  for (const file of files) {
    let inCriterion = false;
    for (const line of fs.readFileSync(path.join(testsDir, file), 'utf8').split(/\r?\n/)) {
      const trimmed = line.trim();
      if (trimmed === '[[criterion]]') {
        inCriterion = true;
        continue;
      }
      if (trimmed.startsWith('[')) inCriterion = false;
      const match = inCriterion ? trimmed.match(/^name\s*=\s*"([a-z0-9_]+)"$/) : null;
      if (match) names.add(match[1]);
    }
  }
  return [...names].sort();
}

function walkFailingSpecs(suite, trail, failing) {
  for (const child of suite?.suites || []) {
    walkFailingSpecs(child, [...trail, child.title].filter(Boolean), failing);
  }
  for (const spec of suite?.specs || []) {
    if (spec.ok === false) failing.push([...trail, spec.title].filter(Boolean).join(' > '));
  }
}

function walkExecutedSpecs(suite, titles) {
  for (const child of suite?.suites || []) walkExecutedSpecs(child, titles);
  for (const spec of suite?.specs || []) {
    const executed = (spec.tests || []).some((test) =>
      (test.results || []).some((result) => result.status !== 'skipped')
    );
    if (executed) titles.push(spec.title);
  }
}

/**
 * Turn a Playwright JSON report + exit code into an honest stage outcome:
 *   { status: 'pass'|'fail', passed, failed, skipped, flaky,
 *     failingTests, warning?, message? }
 * Fails on ANY failed test or a non-zero exit; warns when skipped > passed
 * (skip-stub doctrine).
 */
export function summarizeE2eReport(report, exitCode, outputTail = '') {
  const stats = report?.stats || {};
  const passed = stats.expected ?? 0;
  const failed = stats.unexpected ?? 0;
  const skipped = stats.skipped ?? 0;
  const flaky = stats.flaky ?? 0;
  const failingTests = [];
  const executedTests = [];
  for (const suite of report?.suites || []) {
    walkFailingSpecs(suite, [suite.title].filter(Boolean), failingTests);
    walkExecutedSpecs(suite, executedTests);
  }
  const counts = `passed=${passed} failed=${failed} skipped=${skipped} flaky=${flaky}`;
  if (failed > 0 || failingTests.length > 0) {
    return {
      status: 'fail', passed, failed, skipped, flaky, failingTests, executedTests,
      message: `e2e suite failed (${counts}); failing tests: ${failingTests.join('; ') || 'unreported'}`,
    };
  }
  if (exitCode !== 0) {
    return {
      status: 'fail', passed, failed, skipped, flaky, failingTests, executedTests,
      message: `playwright exited ${exitCode} with no failed tests reported (${counts})${outputTail ? `: ${outputTail}` : ''}`,
    };
  }
  const warning = skipped > passed
    ? `skipped (${skipped}) > passed (${passed}) — suite looks like a skip stub`
    : undefined;
  return { status: 'pass', passed, failed, skipped, flaky, failingTests, executedTests, warning };
}

function taskCoverage(summaries, taskDir) {
  const criterionNames = rubricCriterionNames(taskDir);
  const taskTests = [];
  for (const summary of summaries) {
    for (const title of summary.executedTests || []) {
      if (summary.label === 'task-dir' || !CANONICAL_TEST_TITLES.has(title)) {
        taskTests.push(title);
      }
    }
  }
  const rubricMatched = taskTests.filter((title) =>
    criterionNames.some((name) => title === name || title.endsWith(` ${name}`))
  );
  return {
    taskSpecificTests: taskTests.length,
    rubricMatchedTests: rubricMatched.length,
    rubricCriteria: criterionNames.length,
  };
}

/**
 * Run the task's canonical e2e suite inside `appDir` (where npm ci already
 * ran) against the app the caller is already serving on port 3000.
 * Never throws for suite outcomes; returns
 *   { status: 'skip' } |
 *   { status: 'fail', message, ... } |
 *   { status: 'pass', passed, skipped, flaky, warning?, detail }
 */
export async function runE2eSuite({
  slug,
  appDir,
  runtimeDir,
  taskDir = null,
  requireTaskTests = false,
  timeoutMs = 600000,
}) {
  const invocation = resolveE2eInvocation(appDir);
  if (invocation.mode === 'skip') {
    if (requireTaskTests) {
      return {
        status: 'fail',
        taskSpecificTests: 0,
        rubricMatchedTests: 0,
        rubricCriteria: rubricCriterionNames(taskDir).length,
        message:
          'task-specific Playwright files changed, but no runnable e2e.spec.mjs ' +
          'or e2e/**/*.spec.* suite was discovered',
      };
    }
    return { status: 'skip' };
  }
  if (invocation.mode === 'missing-dependency') {
    return { status: 'fail', message: invocation.message };
  }
  const summaries = [];
  for (const run of invocation.runs) {
    const summary = await runOneSuite({ slug, appDir, runtimeDir, timeoutMs, run });
    summaries.push({ label: run.label, ...summary });
    if (summary.status === 'fail') break; // report the first failure honestly
  }
  const result = mergeSuiteSummaries(summaries);
  const coverage = taskCoverage(summaries, taskDir);
  Object.assign(result, coverage);
  // Executed titles are an internal matching input; keep status artifacts compact.
  delete result.executedTests;
  if (result.status === 'fail') return result;
  if (requireTaskTests && coverage.taskSpecificTests === 0) {
    return {
      ...result,
      status: 'fail',
      message:
        'only the four propagated workspace-contract tests executed; ' +
        'add task-specific Playwright tests derived from rubric criteria',
    };
  }
  if (requireTaskTests && coverage.rubricMatchedTests === 0) {
    return {
      ...result,
      status: 'fail',
      message:
        `${coverage.taskSpecificTests} task-specific tests executed, but none matched a ` +
        'criterion name from tests/<dimension>/<dimension>.toml',
    };
  }
  if (taskDir) {
    result.detail +=
      `; task-specific=${coverage.taskSpecificTests}` +
      ` rubric-matched=${coverage.rubricMatchedTests}`;
  }
  return result;
}

/** Combine per-layout summaries into the stage's single outcome. */
export function mergeSuiteSummaries(summaries) {
  const failed = summaries.find((s) => s.status === 'fail');
  if (failed) {
    return { ...failed, message: `[${failed.label}] ${failed.message}` };
  }
  const total = { status: 'pass', passed: 0, failed: 0, skipped: 0, flaky: 0, failingTests: [] };
  const warnings = [];
  for (const s of summaries) {
    total.passed += s.passed ?? 0;
    total.failed += s.failed ?? 0;
    total.skipped += s.skipped ?? 0;
    total.flaky += s.flaky ?? 0;
    if (s.warning) warnings.push(`[${s.label}] ${s.warning}`);
  }
  if (warnings.length) total.warning = warnings.join('; ');
  total.detail =
    `passed=${total.passed} failed=0 skipped=${total.skipped} flaky=${total.flaky}` +
    ` (suites: ${summaries.map((s) => s.label).join(', ')})`;
  return total;
}

async function runOneSuite({ slug, appDir, runtimeDir, timeoutMs, run }) {
  const reportPath = path.join(runtimeDir, `e2e-report-${slug}-${run.label}.json`);
  let command = run.command;
  if (run.config === 'generated-config') {
    const configPath = path.join(runtimeDir, `e2e-config-${slug}.mjs`);
    fs.writeFileSync(configPath, generatedConfigSource(appDir));
    command = [...command, '-c', configPath];
  } else if (run.config === 'generated-dir-config') {
    const configPath = path.join(runtimeDir, `e2e-dir-config-${slug}.mjs`);
    fs.writeFileSync(configPath, generatedDirConfigSource(appDir));
    command = [...command, '-c', configPath];
  }
  command = [...command, '--reporter=list,json'];

  const output = await new Promise((resolve) => {
    const proc = spawn(command[0], command.slice(1), {
      cwd: appDir,
      env: { ...process.env, CI: '1', PLAYWRIGHT_JSON_OUTPUT_NAME: reportPath },
      stdio: ['ignore', 'pipe', 'pipe'],
    });
    let log = '';
    for (const stream of [proc.stdout, proc.stderr]) {
      stream.on('data', (chunk) => {
        log = (log + chunk.toString()).slice(-12000);
      });
    }
    const timer = setTimeout(() => {
      try { proc.kill('SIGKILL'); } catch { /* already gone */ }
    }, timeoutMs);
    proc.once('close', (code) => {
      clearTimeout(timer);
      resolve({ code: code ?? 1, log: log.trim() });
    });
    proc.once('error', (error) => {
      clearTimeout(timer);
      resolve({ code: 1, log: `${log}\n${error.message}`.trim() });
    });
  });

  let report = null;
  try {
    report = JSON.parse(fs.readFileSync(reportPath, 'utf8'));
  } catch { /* handled below */ }
  const tail = output.log.slice(-3000);
  if (!report) {
    return {
      status: 'fail',
      message: `playwright exited ${output.code} without a JSON report${tail ? `: ${tail}` : ''}`,
    };
  }
  const summary = summarizeE2eReport(report, output.code, tail);
  if (summary.status === 'pass') {
    summary.detail =
      `passed=${summary.passed} failed=0 skipped=${summary.skipped} flaky=${summary.flaky}`;
  }
  return summary;
}
