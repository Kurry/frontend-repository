test('6.10 export_import_recover_without_reload', async ({ page }) => {
  await openLibrary(page)
  const baseline = await exportedDocument(page)
  await confirmDelete(page, 'Executive brief distiller')
  await page.getByRole('button', { name: 'Import JSON' }).click()
  let dialog = dialogByHeading(page, 'Import library JSON')
  await dialog.getByLabel('Library document').fill(JSON.stringify(baseline))
  await dialog.getByRole('button', { name: 'Replace library' }).click()
  expect(await libraryCount(page)).toBe(5)
  await page.getByRole('button', { name: 'Import JSON' }).click()
  dialog = dialogByHeading(page, 'Import library JSON')
  await dialog.getByLabel('Library document').fill('{}')
  await dialog.getByRole('button', { name: 'Replace library' }).click()
  await expect(page.getByText(/does not match/).last()).toBeVisible()
  expect(await libraryCount(page)).toBe(5)
})

test('6.11 export_library_after_save_flow', async ({ page, context }) => {
  await context.grantPermissions(['clipboard-read', 'clipboard-write'])
  const preview = await generateZeroShot(page, 'Export flow prompt')
  const promptText = await preview.innerText()
  await saveCurrentPrompt(page, 'Export flow entry')
  await openLibrary(page)
  let document = await exportedDocument(page)
  expect(document.entries.at(-1)).toMatchObject({ title: 'Export flow entry', technique: 'zero-shot', promptText })
  await page.getByRole('region', { name: 'Export library' }).getByRole('button', { name: 'Copy' }).click()
  expect(JSON.parse(await page.evaluate(() => navigator.clipboard.readText())).entries.at(-1).title).toBe('Export flow entry')
  await confirmDelete(page, 'Export flow entry')
  document = await exportedDocument(page)
  expect(document.entries.some((entry: { title: string }) => entry.title === 'Export flow entry')).toBe(false)
})

test('6.12 import_library_round_trip_flow', async ({ page }) => {
  await generateZeroShot(page, 'Import round trip')
  await saveCurrentPrompt(page, 'Import flow entry')
  await openLibrary(page)
  const exported = await exportedDocument(page)
  await confirmDelete(page, 'Import flow entry')
  await page.getByRole('button', { name: 'Import JSON' }).click()
  const dialog = dialogByHeading(page, 'Import library JSON')
  await dialog.getByLabel('Library document').fill(JSON.stringify(exported))
  await dialog.getByRole('button', { name: 'Replace library' }).click()
  expect((await exportedDocument(page)).entries).toEqual(exported.entries)
})
