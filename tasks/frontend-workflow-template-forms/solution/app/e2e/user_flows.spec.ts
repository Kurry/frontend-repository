test('6.1 generate_and_save_few_shot_end_to_end', async ({ page }) => {
  const form = await fillFewShot(page, 'Label product updates')
  await form.getByRole('button', { name: 'Add example' }).click()
  await form.getByLabel('Example input').nth(1).fill('Resolved a crash')
  await form.getByLabel('Expected output').nth(1).fill('Fix')
  await form.getByRole('button', { name: 'Add document' }).click()
  await page.getByRole('option', { name: /research-notes\.md/ }).click()
  await form.getByRole('button', { name: 'Generate prompt' }).click()
  const preview = page.locator('#prompt-preview pre')
  await expect(preview).toContainText('Added an audit log')
  await expect(preview).toContainText('Resolved a crash')
  await expect(preview).toContainText('research-notes.md')
  await saveCurrentPrompt(page, 'Release classifier')
  await expect(page.getByRole('button', { name: /Few-Shot/ }).first()).toContainText('Saved')
  await openLibrary(page)
  expect(await libraryCount(page)).toBe(6)
})

