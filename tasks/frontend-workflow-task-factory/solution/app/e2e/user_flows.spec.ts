test('6.1 create_task_advances_pipeline_and_metrics', async ({ page }) => {
  await gotoApp(page)
  const before = await repositoryMetrics(page)
  const row = await submitRun(page, { pullRequestNumber: '971', minFiles: '4', maxFiles: '12' })
  const generate = row.locator('.stage-cell').nth(3)
  await expect(generate).toHaveClass(/stage-failed/, { timeout: 6_000 })
  await expect(generate).toContainText('a1')
  await expect(generate).toContainText('a2', { timeout: 3_000 })
  await waitForAcceptedRun(page, '971')
  await page.getByRole('button', { name: 'Repositories', exact: true }).click()
  const after = await repositoryMetrics(page)
  expect(after.tasks).toBe(before.tasks + 1)
  expect(after.processed).toBe(before.processed + 1)
  expect(after.yieldPercent).toBe(Math.round(after.tasks / after.processed * 100))
  await page.getByRole('button', { name: 'Timeline', exact: true }).click()
  await expect(page.getByText('Task accepted').first()).toBeVisible()
  await page.getByRole('button', { name: 'Analytics', exact: true }).click()
  await expect(page.getByRole('heading', { name: 'Tasks per week' })).toBeVisible()
})

