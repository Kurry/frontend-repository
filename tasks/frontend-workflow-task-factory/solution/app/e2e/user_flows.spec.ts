test('6.3 retry_resume_preserves_completed_stages', async ({ page }) => {
  await gotoApp(page)
  const row = await submitRun(page, { pullRequestNumber: '972' })
  const completedBeforeRetry: string[] = []
  const generate = row.locator('.stage-cell').nth(3)
  await expect(generate).toHaveClass(/stage-failed/, { timeout: 6_000 })
  for (let index = 0; index < 3; index += 1) {
    const stage = row.locator('.stage-cell').nth(index)
    await expect(stage).toHaveClass(/stage-complete/)
    completedBeforeRetry.push((await stage.getAttribute('title'))!)
  }
  await expect(generate).toContainText('a2', { timeout: 3_000 })
  for (let index = 0; index < 3; index += 1) await expect(row.locator('.stage-cell').nth(index)).toHaveAttribute('title', completedBeforeRetry[index])
})

