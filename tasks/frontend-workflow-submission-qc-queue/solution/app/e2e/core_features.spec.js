test('1.18 qc_package_export_live', async ({ page }) => {
  await boot(page)
  await openExport(page)
  const json = await exportJson(page)
  expect(json).toMatchObject({ schemaVersion: 1, queue_summary: { total: 12 } })
  expect(json.submissions[0]).toEqual(expect.objectContaining({ id: expect.any(String), title: expect.any(String), contributor_name: expect.any(String), findings: expect.any(Array), history: expect.any(Array) }))
  await page.getByRole('button', { name: /QC report/ }).click()
  await expect(page.locator('.code-window pre')).toContainText('# Arcfield quality-control report')
  const preview = await exportText(page)
  const downloadPromise = page.waitForEvent('download')
  await page.getByRole('button', { name: /Download/ }).click()
  const download = await downloadPromise
  expect(download.suggestedFilename()).toBe('arcfield-qc-report.md')
  expect(await download.createReadStream().then(async (stream) => { let value = ''; for await (const chunk of stream) value += chunk; return value })).toBe(preview)
})

