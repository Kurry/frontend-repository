test('1.20 qc_package_schema_header_and_summary_counts', async ({ page }) => {
  await boot(page)
  await openExport(page)
  const pkg = await exportJson(page)
  expect(pkg.schemaVersion).toBe(1)
  expect(pkg.exported_at).toMatch(/^\d{4}-\d\d-\d\dT.*Z$/)
  expect(Object.values(pkg.queue_summary.stages).reduce((a, b) => a + b, 0)).toBe(pkg.queue_summary.total)
  expect(Object.values(pkg.queue_summary.payout_states).reduce((a, b) => a + b, 0)).toBe(pkg.queue_summary.total)
  const blocker = submissionFromPackage(pkg, titles.blocker)
  expect(blocker).toMatchObject({ gate_status: 'failed', open_finding_counts: { blocker: 1, major: 0, minor: 1 } })
})
