test('1.4 sort_by_finding_count_reverses', async ({ page }) => {
  await boot(page)
  const order = async () => queueRows(page).evaluateAll((rows) => rows.map((row) => row.getAttribute('aria-label')))
  await page.getByLabel('Sort by open finding count').selectOption('asc')
  const ascending = await order()
  await page.getByLabel('Sort by open finding count').selectOption('desc')
  const descending = await order()
  expect(descending).toEqual([...ascending].reverse())
})

